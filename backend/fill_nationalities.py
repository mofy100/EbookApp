def fill_nationalities():
    conn = sqlite3.connect(DB_FILE)
    cursor = conn.cursor()

    updated = 0
    for author_id, nationality in NATIONALITIES.items():
        cursor.execute(
            'UPDATE authors SET nationality = ? WHERE id = ? AND nationality IS NULL',
            (nationality, author_id)
        )
        updated += cursor.rowcount

    conn.commit()

    # 結果サマリ
    cursor.execute('SELECT nationality, COUNT(*) FROM authors GROUP BY nationality ORDER BY COUNT(*) DESC')
    rows = cursor.fetchall()
    print(f"更新件数: {updated}")
    print("\n--- 国籍別内訳 ---")
    for nat, cnt in rows:
        label = nat if nat else '（未設定）'
        print(f"  {label}: {cnt}")

    cursor.execute('SELECT COUNT(*) FROM authors WHERE nationality IS NULL')
    null_count = cursor.fetchone()[0]
    print(f"\n未設定残り: {null_count} 人")

    conn.close()

if __name__ == '__main__':
    fill_nationalities()
